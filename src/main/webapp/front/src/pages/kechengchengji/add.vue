    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            kechengbianhao : false,
            kechengmingcheng : false,
            kaohechengji : false,
            pingyu : false,
            dengjishijian : false,
            xuehao : false,
            xueshengxingming : false,
            gonghao : false,
            jiaoshixingming : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          kechengbianhao: '',
          kechengmingcheng: '',
          kaohechengji: '',
          pingyu: '',
          dengjishijian: '',
          xuehao: '',
          xueshengxingming: '',
          gonghao: '',
